#include<stdio.h>
int main (){
	int x, y,z;
	printf("please enter number 1\n");
	scanf("%d", &x);             			 //first num
	printf("please enter number 2\n");
	scanf("%d", &y);			 			 //second num
	printf("please enter number 3\n");
	scanf("%d", &z);             			 // third num
	printf("number 3 = %d\n",z);			//print in reverse order
	printf("number 2 = %d\n",y);
	printf("number 1 = %d\n",x);
	
}